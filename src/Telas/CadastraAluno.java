    private void cadastrarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cadastrarButtonMouseClicked
        // TODO add your handling code here:
        Aluno pessoa = new Aluno();
        Database conexao = new Database();

        pessoa.setCodigo(Integer.parseInt(codigo.getText()));
        pessoa.setDataCadastro(dataRegistro.getText());
        pessoa.setDataNascimento(dataNascimento.getText());
        pessoa.setNome(nome.getText());
        pessoa.setCpf(cpf.getText());
        pessoa.setRg(rg.getText());
        pessoa.setOrgaoExpedidor(orgaoExpedidor.getText());
        pessoa.setRua(rua.getText());
        pessoa.setNumero(numero.getText());
        pessoa.setBairro(bairro.getText());
        pessoa.setComplemento(complemento.getText());
        pessoa.setMunicipio(municipio.getText());
        pessoa.setCep(cep.getText());
        pessoa.setEmail(email.getText());
        pessoa.setTelefoneFixo(telFixo.getText());
        pessoa.setTelefoneMovel(telMovel.getText());
        pessoa.setCurso(curso.getText());
        pessoa.setEstado((String) comboUF.getSelectedItem());
        pessoa.setInstituicao((String) instituicao.getSelectedItem());
        pessoa.setPeriodo((String) comboPeriodo.getSelectedItem());
        pessoa.setSegunda(segunda.isSelected());
        pessoa.setTerca(terca.isSelected());
        pessoa.setQuarta(quarta.isSelected());
        pessoa.setQuinta(quinta.isSelected());
        pessoa.setSexta(sexta.isSelected());
        pessoa.setSabado(sabado.isSelected());
        if (ativoSim.isSelected()) {
            pessoa.setAtivo(true);
        } else if (ativoNao.isShowing()) {
            pessoa.setAtivo(false);
        }

        if (conexao.cadastraAluno(pessoa)) {
            JOptionPane.showMessageDialog(null, "Cadastrado com sucesso!");
            try {
                Menu.atualizaAlunos(conexao);
            } catch (SQLException ex) {
                Logger.getLogger(CadastraAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                atualizaCodigo();
                limpaCampos();
            } catch (SQLException ex) {
                Logger.getLogger(CadastraAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Aluno não foi cadastrado, verifique!");
        }

    }//GEN-LAST:event_cadastrarButtonMouseClicked


    public void atualizaCodigo() throws SQLException {
        Database conexao = new Database();
        int resp;
        resp = conexao.buscaUltimoCodigo();
        resp = resp + 1;
        codigo.setText(String.valueOf(resp));
        nome.requestFocus();
    }

    public void limpaCampos() {
        dataNascimento.setText(null);
        nome.setText(null);
        cpf.setText(null);
        rg.setText(null);
        orgaoExpedidor.setText(null);
        rua.setText(null);
        numero.setText(null);
        bairro.setText(null);
        complemento.setText(null);
        municipio.setText(null);
        cep.setText(null);
        email.setText(null);
        telFixo.setText(null);
        telMovel.setText(null);
        curso.setText(null);
        comboPeriodo.setSelectedIndex(0);
        instituicao.setSelectedIndex(0);
        comboUF.setSelectedIndex(23);
        nome.requestFocus();
        segunda.setSelected(false);
        terca.setSelected(false);
        quarta.setSelected(false);
        quinta.setSelected(false);
        sexta.setSelected(false);
        sabado.setSelected(false);
        nome.requestFocus();
        ativoSim.setSelected(false);
        ativoNao.setSelected(false);
    }

